struct node
{
    int data;
    struct node* next;
};

//Function to get to the last node
void get_last_node(struct node* p_list)
{
    struct node* run = NULL;

    run = p_list;
    while(run -> next != NULL)
    {
        run = run -> next;
    }

    //run is the last node
}

